    public float stepsToPhase(float steps){
        
        final float phaseF = 2.0f * steps / getDivs();
        return phaseF;
    }
    
    public void addPhaseSteps(float steps){
        final float phaseF = stepsToPhase(steps);
        mf.transPanel.setTransPhase( "a" + phaseF );
    }
    
